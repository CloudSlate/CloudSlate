// Initialize icon animations
function initIconAnimations() {
    // Add hover animations to all icons
    document.querySelectorAll('.icon').forEach(icon => {
        // Add pulse animation on hover
        icon.addEventListener('mouseenter', function() {
            this.classList.add('icon-hover');
        });
        
        icon.addEventListener('mouseleave', function() {
            this.classList.remove('icon-hover');
        });
    });
    
    // Animate icons when they come into view
    const iconObserver = new IntersectionObserver(function(entries) {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('icon-visible');
                // Optional: Unobserve after animation
                // iconObserver.unobserve(entry.target);
            }
        });
    }, {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    });
    
    document.querySelectorAll('.icon-wrapper').forEach(wrapper => {
        iconObserver.observe(wrapper);
    });
}

// Export
window.Icons = Icons;
window.initIconAnimations = initIconAnimations;

// Auto-initialize on DOM ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initIconAnimations);
} else {
    initIconAnimations();
}

